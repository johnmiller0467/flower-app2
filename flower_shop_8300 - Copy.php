      <section id="care-tips">
        <h2>Flower Care — Keep Them Fresh Longer</h2>
        <p>A few simple steps help you get the most from your bouquet:</p>
        <ul>
          <li><strong>Trim the stems:</strong> Cut 1–2 cm at an angle before placing stems in water. Re-trim every 2–3 days.</li>
          <li><strong>Use clean water:</strong> Change the water every 48 hours and clean the vase to prevent bacteria.</li>
          <li><strong>Remove foliage:</strong> Strip leaves that fall below the water line to avoid rot and bacteria growth.</li>
          <li><strong>Keep cool:</strong> Keep bouquets away from direct sunlight, heating vents, and fruit (ethylene gas speeds wilting).</li>
          <li><strong>Flower food:</strong> Use the provided packet — it contains sugar, acid, and a biocide to help stems last longer.</li>
        </ul>
        <p>Specific flowers have specific needs. For example, snapdragons and delphinium prefer cool water and frequent trimming, while succulents need much less water. When in doubt, consult the care card included with each bouquet or contact us for plant-specific advice.</p>
      </section>

      <section id="flowers-encyclopedia">
        <h2>Floral Encyclopedia — Popular Stems &amp; Meanings</h2>
        <p>Below is a helpful guide to common flowers, brief care notes, and symbolic meanings often associated with them.</p>

        <h3>Roses</h3>
        <p>Classic and versatile. Roses come in many shapes and colors. Keep stems cool and change water frequently. Symbolism varies by color: red for love, white for purity, pink for gratitude.</p>

        <h3>Peonies</h3>
        <p>Luxurious, full double blooms usually available in late spring. Peonies are delicate — keep them cool and out of direct sun. They symbolize prosperity, romance, and good fortune.</p>

        <h3>Tulips</h3>
        <p>Spring-blooming with a fresh, modern look. Tulips continue to grow in the vase; trim stems to control height. Associated with perfect love and cheerfulness.</p>

        <h3>Dahlias</h3>
        <p>Showy, sculptural late-summer and autumn blooms. Dahlias can be heavy — provide sturdy support in arrangements. They symbolize elegance and inner strength.</p>

        <h3>Ranunculus</h3>
        <p>Layers of delicate petals create a soft rosette. Ranunculus favor cool conditions and clear water. They signify charm and attractiveness.</p>

        <h3>Hydrangea</h3>
        <p>Large, soft clusters that add volume to any arrangement. Hydrangeas drink a lot of water — keep stems submerged and mist flower heads. They symbolize gratitude and unity.</p>

        <h3>Eucalyptus &amp; Foliage</h3>
        <p>Foliage like eucalyptus, dusted silver or blue, provides texture and scent. Foliage generally lasts longer than many delicate blooms and helps scale an arrangement.</p>

        <p class="note">If you’d like a downloadable care sheet for a particular flower, let us know the stem and we’ll provide a printable guide.</p>
